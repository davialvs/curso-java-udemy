package vetores;

public class Ex02Class {
	
	private String name;
	private double price;
	
	public Ex02Class(String name, double price) {
		this.name = name;
		this.price = price;
	}

	
	//Getter and Setters
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}
	
	
	
	

}
